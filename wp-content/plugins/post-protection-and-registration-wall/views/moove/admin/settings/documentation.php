    <h1>Post Protection and Registration Wall - WordPress Plugin</h1>
    <h3>Overview</h3>
    <ul>
        <li>&bull; Plugin gives you the ability to protect any kind of content on your website u nder a registration wall. *Plugin enables you to protect your posts and pages so that only registered users can view the protected content.</li>
        <li>&bull; Non-members wanting to view a protected page will be asked to login or register.</li>
        <li>&bull; It’s a flexible, well-supported, and easy-to-use WordPress plugin.</li>
    </ul>
    <h3>Default user roles:</h3>
    <p>Choose one from one of three default membership options, then configure the system to fit your website’s goals.</p>
    <ul>
        <li>&bull; Public (visitor not logged in &amp; not registered)</li>
        <li>&bull; Limited (registered user who is logged in)</li>
        <li>&bull; Premium (registered user who is considered a premium user)</li>
    </ul>
    <h3>Restrict Access for Members</h3>
    <ul>
        <li>&bull; Global settings: option to restrict access by membership level</li>
        <li>&bull; Individual settings: option to overwrite global settings by restricting access on individual pages or posts</li>
    </ul>
    <p>Upon activation this plugin creates the following pages that are required for the correct functionality of the plugin: - Login - Register - My Account - Reset Password</p>
    <p>Each page includes the necessary shortcodes that displays the right content.</p>
    <h3>Features</h3>
    <ul>
        <li>&bull; Email Notifications</li>
        <li>&bull; Members Management</li>
        <li>&bull; Assign users to membership levels</li>
        <li>&bull; Protect attachments</li>
        <li>&bull; Show teaser content to convert visitors into members</li>
        <li>&bull; Ability to customize the content protection message that is shown to non-members</li>
        <li>&bull; Ability to customize the confirmation emails sent to members</li>
        <li>&bull; Creates all necessary pages for you upon installation of the plugin: Login, Register, My Account, Reset Password<br /></li>
        <li>&bull; Shortcodes that can display login, registration forms and user profile from anywhere on your site</li>
        <li>&bull; Restrict access to posts &amp; pages based on the user's membership level</li>
        <li>&bull; User Friendly Interface for Content Protection</li>
    </ul>
    <h3>Typical usage</h3>
    <p>Protect your information from users or exchange it for their contact details:</p>
    <ul>
        <li>&bull; Whitepapers</li>
        <li>&bull; Reports</li>
        <li>&bull; Non-public data (intranet)</li>
        <li>&bull; Documents available to members only</li>
    </ul>
    <h3>Additional Features</h3>
