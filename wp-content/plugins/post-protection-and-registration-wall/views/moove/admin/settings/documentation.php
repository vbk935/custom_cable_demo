    </ul>
    <h2 id="installation">Installation</h2>
    <ul>
        <li>&bull; Upload the plugin files to the plugins directory, or install the plugin through the WordPress plugins screen directly.</li>
        <li>&bull; Activate the plugin through the 'Plugins' screen in WordPress</li>
        <li>&bull; Use the Settings-&gt;Moove protection screen to configure the plugin</li>
        <li>&bull; This plugin gives you the ability to protect any kind of content on your website under a registration wall</li>
        <li>&bull; For plugin's ratings and description please visit WordPress.org repository: https://wordpress.org/plugins/post-protection-and-registration-wall/</li>
    </ul>
